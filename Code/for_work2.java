public class for_work2 {
    public static void main(String[]args) {
        int i = 1;
        int m = 5;
        for (;i<=5;i++){
            for (;m>=1;m--)
            System.out.println(i + " + " + m + " = " + (i+m));
        }
        for (int a=0;a<=5;a++){
            System.out.print(a+"+"+(5-a)+"="+(a+(5-a)));
          }
    }
}
